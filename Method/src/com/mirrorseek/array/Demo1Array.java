package com.mirrorseek.array;
/*
* 数组的概念：是一种容器
* 数组的特点：
* 1：数组是一种引用数据类型
* 2：数组当中的多个数据，类型必须统一
* 3：数组的长度在程序运行期间不可改变
*
* 两种常见的初始化方式
* 1：静态初始化
* 2：动态初始化
*
* 动态初始化数组的格式：
* 数据类型[] 数组名称 = new 数据类型[数组长度];
*
* */
public class Demo1Array {
    public static void main(String[] args) {
//        动态数组
        int[] arrayB = new int[100];
        double[] arrayA = new double[10];
        char[] arrayC = new char[50];
    }
}
